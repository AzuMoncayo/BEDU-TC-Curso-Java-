public class Main {
    public static void main(String[] args) {
        MetodoPago[] pagos = {
                new PagoEfectivo(150.0),
                new PagoTarjeta(320.0, 500.0),
                new PagoTransferencia(700.0, false) // Falla
        };

        for (MetodoPago pago : pagos) {
            if (pago instanceof Autenticable autenticable) {
                if (autenticable.autenticar()) {
                    System.out.println("✅ Autenticación exitosa.");
                    pago.procesarPago();
                    pago.mostrarResumen();
                } else {
                    System.out.println("❌ Fallo de autenticación. " + pago.getClass().getSimpleName() + " no válido.");
                }
                System.out.println(); // Separador visual
            }
        }
    }
}
